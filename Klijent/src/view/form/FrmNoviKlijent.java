    private boolean validateForm() {
        boolean flag = false;
        if(txtJMBG.getText().trim().isEmpty()){
            flag = true;
            lblErrorJMBG.setText("Morate uneti JMBG klijenta!");
            lblErrorJMBG.setVisible(true);
        }
        
        if(txtIme.getText().trim().isEmpty()){
            flag = true;
            lblErrorIme.setText("Morate uneti ime klijenta!");
            lblErrorIme.setVisible(true);
        }
        
        if(txtPrezime.getText().trim().isEmpty()){
            flag = true;
            lblErrorPrezime.setText("Morate uneti prezime klijenta!");
            lblErrorPrezime.setVisible(true);
        }
        
        if(txtEmail.getText().trim().isEmpty()){
            flag = true;
            lblErrorEmail.setText("Morate uneti e-mail klijenta!");
            lblErrorEmail.setVisible(true);
        }
        
        if(txtAdresa.getText().trim().isEmpty()){
            flag = true;
            lblErrorAdresa.setText("Morate uneti adresu klijenta!");
            lblErrorAdresa.setVisible(true);
        }
        
        
        return flag;
    }

    private void srediFormu() {
        lblErrorJMBG.setVisible(false);
        lblErrorIme.setVisible(false);
        lblErrorPrezime.setVisible(false);
        lblErrorEmail.setVisible(false);
        lblErrorAdresa.setVisible(false);
    }
