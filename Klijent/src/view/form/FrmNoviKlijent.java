    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try{
            if(validateForm()){
                return;
            }
            Klijent klijent = new Klijent();
            
            klijent.setJMBG(txtJMBG.getText().trim());
            klijent.setIme(txtIme.getText().trim());
            klijent.setPrezime(txtPrezime.getText().trim());
            klijent.setEmail(txtEmail.getText().trim());
            klijent.setAdresa(txtAdresa.getText().trim());
            
            Kontroler.getInstance().dodajKlijenta(klijent);
            
            JOptionPane.showMessageDialog(this, "Klijent uspesno dodat!");
            txtJMBG.setText("");
            txtIme.setText("");
            txtPrezime.setText("");
            txtEmail.setText("");
            txtAdresa.setText("");
            lblErrorJMBG.setVisible(false);
            lblErrorIme.setVisible(false);
            lblErrorPrezime.setVisible(false);
            lblErrorAdresa.setVisible(false);
            lblErrorEmail.setVisible(false);
        } catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Greska: \n" + ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDodajActionPerformed

    

