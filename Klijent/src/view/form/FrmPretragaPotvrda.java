    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        Klijent k = (Klijent) cmbKlijent.getSelectedItem();
        
        try {
            List<PotvrdaOIzdavanju> pretrazenePotvrde = Kontroler.getInstance().getPotvrdeByKlijent(k);
            
            potvrde = pretrazenePotvrde;
            TableModelPotvrda tmp = (TableModelPotvrda) tblPotvrde.getModel();
            tmp.setPotvrde(potvrde);
            tmp.fireTableDataChanged();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Neuspesna pretraga potvrda!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnPretraziActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        int selected = tblPotvrde.getSelectedRow();
        
        if (selected != -1) {
            PotvrdaOIzdavanju potvrda = potvrde.get(selected);
            FrmIzmenaPotvrde fip = new FrmIzmenaPotvrde(parent, true, potvrda);
            fip.setVisible(true);
            osveziTabelu();
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        int selected = tblPotvrde.getSelectedRow();
        
        if (selected != -1) {
            PotvrdaOIzdavanju potvrda = potvrde.get(selected);
            try {
                Kontroler.getInstance().obrisiPotvrdu(potvrda);
                osveziTabelu(selected);
                JOptionPane.showMessageDialog(this, "Uspesno obrisana potvrda!", "Potvrda", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Za izabranu potvrdu vec postoje izdavanja!", "Greska", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Morate odabrati potvrdu!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnPrikaziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrikaziActionPerformed
        int selected = tblPotvrde.getSelectedRow();
        
        if (selected != - 1) {
            new FrmPrikaziPotvrdu(parent, true, potvrde.get(selected)).setVisible(true);
        } else{
            JOptionPane.showMessageDialog(this, "Morate odabrati potvrdu!", "Greska", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPrikaziActionPerformed
    
    private void srediFormu() throws Exception {
        postaviModel();
        ucitajKlijente();
    }

