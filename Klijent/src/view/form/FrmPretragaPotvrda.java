    private void postaviModel() throws Exception {
        potvrde = new ArrayList<>();
        
        potvrde = Kontroler.getInstance().getPotvrde();
        System.out.println("Uspesno ucitana lista potvrda!");
        
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);
        
        TableModelPotvrda tmp = new TableModelPotvrda(potvrde);
        tblPotvrde.setModel(tmp);
        tblPotvrde.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
        tblPotvrde.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
    }
    
    private void ucitajKlijente() throws Exception {
        cmbKlijent.removeAllItems();
        
        List<Klijent> klijenti = Kontroler.getInstance().getKlijenti();
        
        for (Klijent klijent : klijenti) {
            cmbKlijent.addItem(klijent);
        }
        
        System.out.println("Klijenti uspesno ucitani!");
    }
    
    private void osveziTabelu(int index) {
        TableModelPotvrda tmp = (TableModelPotvrda) tblPotvrde.getModel();
        tmp.getPotvrde().remove(index);
        tmp.fireTableDataChanged();
    }
    
    private void osveziTabelu() {
        TableModelPotvrda tmp = (TableModelPotvrda) tblPotvrde.getModel();
        tmp.fireTableDataChanged();
    }
